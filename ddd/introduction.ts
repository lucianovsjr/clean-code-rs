/**
 * DDD - Domain Driven Design
 * 
 * Desenhar como será o seu software.
 * Só faz sentido estudar se já sentio a dor que ddd resolve.
 * 
 * Domain: Problema que você está resolvendo. Seu contexto.
 * Subdomínios: Areas de entendimento dentro de Domain.
 * Ex: Domínio Ecommerce, Subdomínios Logística, Faturamento, Pagamento, Estoque...
 * Um Subdomínio pode ser um microservicço.
 * 
 * Domain experts: Possui um conhecimento profundo sobre o Subdomínio.
 * 
 * Entidades: Atores dentro de cada subdomínio.
 * Casos de uso: Fluxo.
 * Ex: Pegar as ordens de pedido e emitir notas fiscais.
 * 
 * Uma entidade em subdomínios diferentes podem ter nomes diferentes.
 */

/**
 * Software é um organismo vivo, não é uma representação das tabelas.
 * 
 * Podemos ter uma única tabela para customer e recipient.
 * As entidades não devem ser representadas em tabelas.
 */